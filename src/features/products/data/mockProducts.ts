export function calculateProductStats(products: Product[]) {
  const stats = {
    total: products.length,
    active: 0,
    inactive: 0,
    outOfStock: 0,
    draft: 0,
    totalValue: 0,
  }

  products.forEach((product) => {
    if (product.status === 'active') stats.active++
    if (product.status === 'inactive') stats.inactive++
    if (product.status === 'out_of_stock') stats.outOfStock++
    if (product.status === 'draft') stats.draft++
    stats.totalValue += product.price * product.stock
  })

  return stats
}
