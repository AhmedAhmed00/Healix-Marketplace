export function calculateAdStats(ads: Ad[]) {
  const stats = {
    total: ads.length,
    active: 0,
    inactive: 0,
    pending: 0,
    expired: 0,
    totalBudget: 0,
    totalSpent: 0,
    totalImpressions: 0,
    totalClicks: 0,
  }

  ads.forEach((ad) => {
    if (ad.status === 'active') stats.active++
    if (ad.status === 'inactive') stats.inactive++
    if (ad.status === 'pending') stats.pending++
    if (ad.status === 'expired') stats.expired++
    stats.totalBudget += ad.budget
    stats.totalSpent += ad.spent
    stats.totalImpressions += ad.impressions
    stats.totalClicks += ad.clicks
  })

  return stats
}
